package task;

public class localvariable {

	public static void main(String[] args) {
		// Local variable
		int x = 10;
		int y = 20;
		int sum = x + y;

		System.out.println("Value of x: " + x);
		System.out.println("Value of y: " + y);
		System.out.println("Sum of x and y: " + sum);
	
	}

}
