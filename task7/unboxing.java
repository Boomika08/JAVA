package task;

public class unboxing {

	public static void main(String[] args) {
		Integer i=30;//class type Integer
		int a=i;//converting class type to primitive data type implicitly by the compiler
		System.out.println("Primitive datatype int: "+a);
		
		Character c='h';//class type Character
		char b=c;//converting class type to primitive data type implicitly by the compiler
		System.out.println("Primitive datatype int: "+b);
	}

}

 